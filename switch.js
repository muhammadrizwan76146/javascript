function testingSwitch() {
    var nilai = 6;

    switch(nilai) {
        case 10:
            console.log("sangat bagus");
            break;
        case 8:
            console.log("Baik");
            break;
        case 6:
            console.log("cukup");
            break;
        default:
            console.log("lainnya");
    }
}

testingSwitch();