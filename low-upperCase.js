function ubah(){
    var huruf = "Belajar Javascript";

    console.log(huruf.toLowerCase()); // merubah ke lowerCase atau membuat huruf kecil semua. jadi hasilnya: "belajar javascript"
    console.log(huruf.toUpperCase());  // jika ingin membuat hurup besar semua maka kodenya adalah toUpperCase
}

ubah();