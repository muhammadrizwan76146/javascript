// ceil : membulatkan ke atas
// floor : membulatkan ke bawah
// round : membulatkan ke paling dekat

// contoh:
var x = 12.3;

console.log(Math.ceil(x)); // membulatkan ke atas, jadi 13
console.log(Math.floor(x)); // membulatkan ke bawah, jadi 12
console.log(Math.round(x)); // membulatkan ke paling dekat, jadi 12