function stringRepeat(){
    var kata = "Ayo Belajar ...";

    /* fungsi repeat adalah untuk mengulang sebuah string */

    // contoh mengulah string dari variabel
    console.log(kata.repeat(3)); // mengulang string dari variabel 'kata' sebanyak 3 kali

    // contoh mengulang string langsung pada console
    console.log('Ini Buku ...'.repeat(4)); // mgnulang string sebanyak 4 kali
}

stringRepeat();